package matematica;

public class MenorNumero {

    public static void menorEntreEles(int n1, int n2) {
        //isso é uma funcao
        if (n1 < n2) {
            System.out.println("o numero" + n1 + "é o menor");
        } else {
            System.out.println("o numero" + n2 + "é o menor");
        }
    }

}
