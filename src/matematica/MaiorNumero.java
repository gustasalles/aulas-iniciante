package matematica;

public class MaiorNumero {

    public static void maiorEntreEles(int numero1, int numero2) {

        if (numero1 > numero2) {
            System.out.println("o numero " + numero1 + "é o maior");

        } else {
            System.out.println("o numero " + numero2 + "é o maior");
        }

    }

}
